package vo;

public class FaqInfo {
	private int fq_idx, fq_read, al_idx;
	private String fq_writer, fq_title, fq_content, fq_date, fq_status;
	public int getFq_idx() {
		return fq_idx;
	}
	public void setFq_idx(int fq_idx) {
		this.fq_idx = fq_idx;
	}
	public int getFq_read() {
		return fq_read;
	}
	public void setFq_read(int fq_read) {
		this.fq_read = fq_read;
	}
	public int getAl_idx() {
		return al_idx;
	}
	public void setAl_idx(int al_idx) {
		this.al_idx = al_idx;
	}
	public String getFq_writer() {
		return fq_writer;
	}
	public void setFq_writer(String fq_writer) {
		this.fq_writer = fq_writer;
	}
	public String getFq_title() {
		return fq_title;
	}
	public void setFq_title(String fq_title) {
		this.fq_title = fq_title;
	}
	public String getFq_content() {
		return fq_content;
	}
	public void setFq_content(String fq_content) {
		this.fq_content = fq_content;
	}
	public String getFq_date() {
		return fq_date;
	}
	public void setFq_date(String fq_date) {
		this.fq_date = fq_date;
	}
	public String getFq_status() {
		return fq_status;
	}
	public void setFq_status(String fq_status) {
		this.fq_status = fq_status;
	}
	
	
}
