package vo;

public class NoticeInfo {
	private int nl_idx, nl_read, al_idx;
	private String nl_writer, nl_title, nl_content, nl_date, nl_status;
	
	public int getNl_idx() {
		return nl_idx;
	}
	public void setNl_idx(int nl_idx) {
		this.nl_idx = nl_idx;
	}
	public int getNl_read() {
		return nl_read;
	}
	public void setNl_read(int nl_read) {
		this.nl_read = nl_read;
	}
	public int getAl_idx() {
		return al_idx;
	}
	public void setAl_idx(int al_idx) {
		this.al_idx = al_idx;
	}
	public String getNl_writer() {
		return nl_writer;
	}
	public void setNl_writer(String nl_writer) {
		this.nl_writer = nl_writer;
	}
	public String getNl_title() {
		return nl_title;
	}
	public void setNl_title(String nl_title) {
		this.nl_title = nl_title;
	}
	public String getNl_content() {
		return nl_content;
	}
	public void setNl_content(String nl_content) {
		this.nl_content = nl_content;
	}
	public String getNl_date() {
		return nl_date;
	}
	public void setNl_date(String nl_date) {
		this.nl_date = nl_date;
	}
	public String getNl_status() {
		return nl_status;
	}
	public void setNl_status(String nl_status) {
		this.nl_status = nl_status;
	}

}