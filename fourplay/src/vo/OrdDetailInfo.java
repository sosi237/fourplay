package vo;

public class OrdDetailInfo {
	private String ol_id, pl_id, pl_name, pl_img1, od_opt;
	private int od_idx, od_cnt, od_price;
	
	public String getOl_id() {
		return ol_id;
	}
	public void setOl_id(String ol_id) {
		this.ol_id = ol_id;
	}
	public String getPl_id() {
		return pl_id;
	}
	public void setPl_id(String pl_id) {
		this.pl_id = pl_id;
	}
	public String getPl_name() {
		return pl_name;
	}
	public void setPl_name(String pl_name) {
		this.pl_name = pl_name;
	}
	public String getPl_img1() {
		return pl_img1;
	}
	public void setPl_img1(String pl_img1) {
		this.pl_img1 = pl_img1;
	}
	public String getOd_opt() {
		return od_opt;
	}
	public void setOd_opt(String od_opt) {
		this.od_opt = od_opt;
	}
	public int getOd_idx() {
		return od_idx;
	}
	public void setOd_idx(int od_idx) {
		this.od_idx = od_idx;
	}
	public int getOd_cnt() {
		return od_cnt;
	}
	public void setOd_cnt(int od_cnt) {
		this.od_cnt = od_cnt;
	}
	public int getOd_price() {
		return od_price;
	}
	public void setOd_price(int od_price) {
		this.od_price = od_price;
	}
}
